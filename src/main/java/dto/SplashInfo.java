package dto;

public class SplashInfo {

    private String splashId;
    private String splashUrl;
    private String splashToUrl;
    private String splashTime;

    public String getSplashId() {
        return splashId;
    }

    public void setSplashId(String splashId) {
        this.splashId = splashId;
    }

    public String getSplashTime() {
        return splashTime;
    }

    public void setSplashTime(String splashTime) {
        this.splashTime = splashTime;
    }

    public String getSplashToUrl() {
        return splashToUrl;
    }

    public void setSplashToUrl(String splashToUrl) {
        this.splashToUrl = splashToUrl;
    }

    public String getSplashUrl() {
        return splashUrl;
    }

    public void setSplashUrl(String splashUrl) {
        this.splashUrl = splashUrl;
    }

}
